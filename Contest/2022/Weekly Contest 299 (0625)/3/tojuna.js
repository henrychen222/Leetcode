const maximumsSplicedArray = (a, b) => {
    let n = a.length, d1 = [], d2 = [], sumA = sm(a), sumB = sm(b);
    for (let i = 0; i < n; i++) {
        d1.push(a[i] - b[i]);
        d2.push(b[i] - a[i]);
    }
    // pr(maxSubArray(d2), maxSubArray(d1))
    return Math.max(sumA + maxSubArray(d2), sumB + maxSubArray(d1));
};

const maxSubArray = (a) => {
    let n = a.length;
    let dp = Array(n).fill(0);
    dp[0] = a[0];
    for (let i = 1; i < n; i++) dp[i] = Math.max(dp[i - 1], 0) + a[i];
    return Math.max(...dp);
};

